</ul>


<div class="inner-copy article-full-content">
    <p>Donating blood regularly has proved beneficial not only for the recipient but also the donor. It is helpful for the vital organs, and it reduces risk for chronic diseases such as cancer and stroke.</p>
<p>For certain treatments like chemotherapy, blood is required on a daily basis, whereas victims of accident may require up to 100 units of blood transfusion. In India, blood shortage is more than 2 million pint and counting.</p>
<p>This World Blood Donor Day,  here are 13 reasons why you should donate blood:</p>
<div class="dm-player" owners="firstpost" cpeid="5df6e56e3e8c0962957f6a76" sort="recent" style="margin-bottom:30px" getupdatedvideo="false" showadonly="true" pipAtStart="true" showCloseButtonPip="true"></div><p><a href="http://www.firstpost.com/wp-content/uploads/2017/06/blood-donor-2.jpg"><img class="alignnone size-full wp-image-3594423"  src="//www.firstpost.com/wp-content/uploads/2017/06/blood-donor-2.jpg"  alt="World Blood Donor Day 13 Reasons why you should donate blood" width="825" height="500" /></a></p>
<p><strong>1. Reduce risk of heart attacks and liver ailment </strong></p>
<p>Donating blood regularly is beneficial to prevent and reduce heart attacks and liver ailment. The risk of heart and liver related problem is caused by the iron overload in the body. Donating blood helps in maintaining the iron level in the body and thus reduce those risk.</p>
<p>&nbsp;</p>
<p class="bgimg"><a href="http://www.firstpost.com/wp-content/uploads/2017/06/Untitled-design7.jpg"><img class="alignnone size-full wp-image-3594393"  src="//www.firstpost.com/wp-content/uploads/2017/06/Untitled-design7.jpg"  alt="World Blood Donor Day 13 Reasons why you should donate blood" width="825" height="500" /></a></p>
<p><strong>2. Lower the risk of cancer </strong></p>
<p>Cancer is the most feared and deadly disease. Blood donation helps in lowering the risk of cancer. By donating blood regularly the iron level in the blood is balanced and the risk of cancer-related to the liver, lungs, and intestine gets lower.</p>
<p class="bgimg"><a href="http://www.firstpost.com/wp-content/uploads/2017/06/what-is-cancer.jpg"><img class="alignnone size-full wp-image-3594307"  src="//www.firstpost.com/wp-content/uploads/2017/06/what-is-cancer.jpg"  alt="World Blood Donor Day 13 Reasons why you should donate blood" width="825" height="500" /></a></p>
<p>&nbsp;</p>
<p><strong>3. New blood cells</strong></p>
<p>Once we donate blood, the body tries to restore the blood loss. This helps in the production of the new blood cells and maintain good health.</p>
<p class="bgimg"><a href="http://www.firstpost.com/wp-content/uploads/2017/06/RBCsWBCsPlatelets.jpg"><img class="alignnone size-full wp-image-3594311"  src="//www.firstpost.com/wp-content/uploads/2017/06/RBCsWBCsPlatelets.jpg"  alt="World Blood Donor Day 13 Reasons why you should donate blood" width="825" height="500" /></a></p>
<p><strong>4. Reduced risk of hemochromatosis</strong></p>
<p>Hemochromatosis is a disease that occurs due to increase in the absorption of iron by the body. Blood donation helps in reducing iron overload in the body and prevent Hemochromatosis.</p>
<p class="bgimg"><a href="http://www.firstpost.com/wp-content/uploads/2017/06/Hemochromatosis.jpg"><img class="alignnone size-full wp-image-3594315"  src="//www.firstpost.com/wp-content/uploads/2017/06/Hemochromatosis.jpg"  alt="World Blood Donor Day 13 Reasons why you should donate blood" width="825" height="500" /></a></p>
<p><strong>5. Maintain Weight </strong></p>
<p>It is recommended to donate blood for those who are overweight. Regularly donating blood helps in weight loss and burn fat up to 650 calories.</p>
<p>&nbsp;</p>
<p class="bgimg"><a href="http://www.firstpost.com/wp-content/uploads/2017/06/MaintainWeight.jpg"><img class="alignnone size-full wp-image-3594317"  src="//www.firstpost.com/wp-content/uploads/2017/06/MaintainWeight.jpg"  alt="World Blood Donor Day 13 Reasons why you should donate blood" width="825" height="500" /></a></p>
<p><strong>6. Helps prevent premature ageing.</strong></p>
<p>While donating blood, you not only lose weight but it also helps in reducing stress. Stress is one of the reasons that triggers premature ageing. Blood donation helps in reducing stress on your mind and body. Also, keeps the skin tight and wrinkle-free.</p>
<p class="bgimg"><a href="http://www.firstpost.com/wp-content/uploads/2017/06/prematureageing.jpg"><img class="alignnone size-full wp-image-3594319"  src="//www.firstpost.com/wp-content/uploads/2017/06/prematureageing.jpg"  alt="World Blood Donor Day 13 Reasons why you should donate blood" width="825" height="516" /></a></p>
<p><strong> 7. </strong><strong>Speeds up healing process</strong></p>
<p>The body tries to adjust to the loss of red blood the cells we donate blood, these adjustments also help in accelerating the wound healing process.</p>
<p class="bgimg"><a href="http://www.firstpost.com/wp-content/uploads/2017/06/healing-process.jpg"><img class="alignnone size-full wp-image-3594321"  src="//www.firstpost.com/wp-content/uploads/2017/06/healing-process.jpg"  alt="World Blood Donor Day 13 Reasons why you should donate blood" width="825" height="500" /></a></p>
<p><strong>8. Lower cholesterol level</strong></p>
<p>Blood contains iron, if the iron in blood is overloaded it can increase the chances of blockage in blood vessels. Blood donation can help to reduce the amount of iron in the blood thus helps in lower cholesterol.</p>
<p class="bgimg"><a href="http://www.firstpost.com/wp-content/uploads/2017/06/cholesterol-level.jpg"><img class="alignnone size-full wp-image-3594325"  src="//www.firstpost.com/wp-content/uploads/2017/06/cholesterol-level.jpg"  alt="World Blood Donor Day 13 Reasons why you should donate blood" width="825" height="500" /></a></p>
<p><strong>9. Free Medical checkup</strong></p>
<p>Every donor goes through a routine checkup prior to donation. Body temperature is checked along with blood pressure, hemoglobin and pulse. Blood is tested for 13 infectious disease like HIV, West Nile Virus, hepatitis B and C and Syphilis. It gives you a cost free quick look into your health.</p>
<p>&nbsp;</p>
<p class="bgimg"><a href="http://www.firstpost.com/wp-content/uploads/2017/06/FreeMedicalcheckup.jpg"><img class="alignnone size-full wp-image-3594327"  src="//www.firstpost.com/wp-content/uploads/2017/06/FreeMedicalcheckup.jpg"  alt="World Blood Donor Day 13 Reasons why you should donate blood" width="825" height="500" /></a></p>
<p><strong>10. Live a longer life</strong></p>
<p>The people who involve in the altruistic work have proven to live a longer life. Blood donation is altruistic works so it not only save lives of other but also helps you live longer and healthier.</p>
<p class="bgimg"><a href="http://www.firstpost.com/wp-content/uploads/2017/06/100th-birthday-cake-007.jpg"><img class="alignnone size-full wp-image-3594329"  src="//www.firstpost.com/wp-content/uploads/2017/06/100th-birthday-cake-007.jpg"  alt="World Blood Donor Day 13 Reasons why you should donate blood" width="825" height="500" /></a></p>
<p><strong>11. Psychological Upliftment</strong></p>
<p>Beside all the heathy benefits that we obtain by donating blood, we also get the powerful benefit psychologically by helping the one in need.</p>
<p class="bgimg"><a href="http://www.firstpost.com/wp-content/uploads/2017/06/psyuplift.jpg"><img class="alignnone size-full wp-image-3594331"  src="//www.firstpost.com/wp-content/uploads/2017/06/psyuplift.jpg"  alt="World Blood Donor Day 13 Reasons why you should donate blood" width="825" height="500" /></a></p>
<p><strong>12. Blood storage </strong></p>
<p>There is no other method to manufacture blood; the only way to collect blood is from the people who are willing to donate. The blood that we donate is also stored at the blood bank after their usage and used in future when needed. Some chemicals are added to it in order to preserve it for long time and use it in future when needed.</p>
<p class="bgimg"><a href="http://www.firstpost.com/wp-content/uploads/2017/06/Bloodstorage.jpg"><img class="alignnone size-full wp-image-3594333"  src="//www.firstpost.com/wp-content/uploads/2017/06/Bloodstorage.jpg"  alt="World Blood Donor Day 13 Reasons why you should donate blood" width="825" height="500" /></a></p>
<p><strong> 13. </strong><strong>Save lives</strong></p>
<p>Every time we donate one pint of blood it helps save three lives, so if we donate four times in a year we end up saving 12 lives. We don’t have to be a superhero to save someone, a simple act of donating blood can also save lives.</p>
<p class="bgimg"><a href="http://www.firstpost.com/wp-content/uploads/2017/06/Savelives.jpg"><img class="alignnone size-full wp-image-3594339"  src="//www.firstpost.com/wp-content/uploads/2017/06/Savelives.jpg"  alt="World Blood Donor Day 13 Reasons why you should donate blood" width="800" height="500" /></a></p>

